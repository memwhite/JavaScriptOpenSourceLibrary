//Ternary operators

//evalutes the statement, and if it's true, it returns whatever is on the left side of the colon (:)
//if it's false, it will return what's on the right side of the colon (:)

var harryPotter = "wizard";


//returns: You're a wizard Harry
harryPotter === "wizard" ? console.log("You're a wizard Harry") : console.log("You're a muggle Harry");

//returns: You're a muggle Harry
harryPotter === "not a wizard" ? console.log("You're a wizard Harry") : console.log("You're a muggle Harry"); 


